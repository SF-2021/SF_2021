<h1 class="title">Правильные и неправильные глаголы в английском языке</h1>

<div id="partnerPnt"></div>

<div class="article" data-sound-type="grammar" data-sound-file="regular-verbs">
    <p>
        По способу образования форм прошедшего неопределенного времени и причастия II все <a href="/grammar/english-verbs">английские глаголы</a> делятся на правильные и неправильные.</p>
    <h2 class="mtitle">
        Правильные глаголы</h2>
    <p>
        Большая часть английских глаголов принадлежит к правильным глаголам, образование формы прошедшего неопределенного времени и причастие II&nbsp; у которых осуществляется при помощи окончания <b>-ed</b>:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="1">work</span> &ndash; <span class="eng sound-word" data-word="2">work<b>ed</b></span> &ndash; <span class="eng sound-word" data-word="2">work<b>ed</b></span></div>
        <div class="example__line">
            <span class="eng sound-word" data-word="4">watch</span> &ndash; <span class="eng sound-word" data-word="5">watch<b>ed</b></span> &ndash; <span class="eng sound-word" data-word="5">watch<b>ed</b></span></div>
    </div>
    <p>
        При этом:</p>
    <ul class="list">
        <li class="list__item">
            Если основа глагола оканчивается на согласную и <b>-y</b>, то y меняется на <b>-i</b>:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="45">cr<b>y</b></span> &ndash; <span class="eng sound-word" data-word="46">cr<b>ied</b></span> &ndash; <span class="eng sound-word" data-word="46">cr<b>ied</b></span></div>
    </div>
    <ul class="list">
        <li class="list__item">
            Если основа оканчивается на <b>-e</b>, то после прибавления <b>-ed</b> пишется только одна буква <b>-e</b>:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="10">translat<b>e</b></span> &ndash; <span class="eng sound-word" data-word="11">translat<b>ed</b></span> &ndash; <span class="eng sound-word" data-word="11">translat<b>ed</b></span></div>
    </div>
    <ul class="list">
        <li class="list__item">
            Если основа оканчивается на ударную краткую гласную и согласную после нее, то во 2 и 3 форме согласная в конце слова удваивается:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="13">admi<b>t</b></span> &ndash; <span class="eng sound-word" data-word="14">admi<b>tted</b></span> &ndash; <span class="eng sound-word" data-word="14">admi<b>tted</b></span></div>
        <div class="example__line">
            <span class="eng sound-word" data-word="16">sli<b>p</b></span> &ndash; <span class="eng sound-word" data-word="17">sli<b>pped</b></span> &ndash; <span class="eng sound-word" data-word="17">sli<b>pped</b></span></div>
    </div>
    <div class="remark">
        Примечание:</div>
    <p>
        В британском английском, если эта конечная согласная <b>-l</b>, то она удваивается независимо от ударения:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="19">to trave<b>l</b></span> &ndash; <span class="eng sound-word" data-word="20">trave<b>lled</b></span></div>
    </div>
    <a name="irregular-verbs"></a>
    <div id="spot-adcom"></div><h2 class="mtitle">
        Неправильные глаголы</h2>
    <p>
        В английском языке есть также группа глаголов, для которых вторая и третья форма образуются особым образом. Их называют <a href="/grammar/irregular-verbs">неправильными английскими глаголами</a>.</p>
    <p>
        Можно выделить несколько <strong>основных способов образования форм неправильных глаголов</strong>:</p>
    <ul class="list">
        <li class="list__item">
            Прибавление окончания <b>-t</b> или <b>-d</b> с изменением или сохранением гласной в корне:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="21">burn</span> &ndash; <span class="eng sound-word" data-word="22">burn<b>t</b></span> &ndash; <span class="eng sound-word" data-word="22">burn<b>t</b></span></div>
        <div class="example__line">
            <span class="eng sound-word" data-word="24">keep</span> &ndash; <span class="eng sound-word" data-word="25">kep<b>t</b></span> &ndash; <span class="eng sound-word" data-word="25">kep<b>t</b></span></div>
    </div>
    <ul class="list">
        <li class="list__item">
            Замена конечной <b>-d</b> на <b>-t</b>:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="27">spen<b>d</b></span> &ndash; <span class="eng sound-word" data-word="28">spen<b>t</b></span> &ndash; <span class="eng sound-word" data-word="28">spen<b>t</b></span></div>
        <div class="example__line">
            <span class="eng sound-word" data-word="30">sen<b>d</b></span> &ndash; <span class="eng sound-word" data-word="31">sen<b>t</b></span> &ndash; <span class="eng sound-word" data-word="31">sen<b>t</b></span></div>
    </div>
    <ul class="list">
        <li class="list__item">
            Изменение корневой гласной во 2-ой форме и сохранение или изменение корневой гласной с добавлением окончания <b>-e(n)</b>:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="33">write</span> &ndash; <span class="eng sound-word" data-word="34">wr<b>o</b>te</span> &ndash; <span class="eng sound-word" data-word="35">writ<b>ten</b></span></div>
        <div class="example__line">
            <span class="eng sound-word" data-word="36">drive</span> &ndash; <span class="eng sound-word" data-word="37">dr<b>o</b>ve</span> &ndash; <span class="eng sound-word" data-word="38">dri<b>ven</b></span></div>
    </div>
    <ul class="list">
        <li class="list__item">
            Совпадение всех трех форм:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="39">cost</span> &ndash; <span class="eng sound-word" data-word="39">cost</span> &ndash; <span class="eng sound-word" data-word="39">cost</span></div>
        <div class="example__line">
            <span class="eng sound-word" data-word="42">put</span> &ndash; <span class="eng sound-word" data-word="42">put</span> &ndash; <span class="eng sound-word" data-word="42">put</span></div>
    </div>

