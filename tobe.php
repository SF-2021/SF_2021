<h1 class="title">Глагол to be в английском языке</h1>

<div id="partnerPnt"></div>

<div class="article" data-sound-type="grammar" data-sound-file="verb-to-be">


    <p>
        Значение глагола <strong>to be</strong> - &quot;быть, находиться&quot;. В отличие от других <a href="/grammar/english-verbs">английских глаголов</a>, <strong>глагол to be</strong> спрягается (т.е. изменяется по лицам и числам).</p>
    <h2 class="mtitle">
        Формы глагола to be</h2>
    <table class="table table_bordered table_centered">
        <tbody>
        <tr>
            <td>
                I am</td>
            <td>
                я есть (существую)</td>
        </tr>
        <tr>
            <td>
                He / She / It is</td>
            <td>
                он, она, оно есть (существует)</td>
        </tr>
        <tr>
            <td>
                We are</td>
            <td>
                мы есть (существуем)</td>
        </tr>
        <tr>
            <td>
                You are</td>
            <td>
                ты, вы есть (существуете)</td>
        </tr>
        <tr>
            <td>
                Тhey are</td>
            <td>
                они есть (существуют)</td>
        </tr>
        </tbody>
    </table>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="1"><b>I am</b> in the room.</span><br />
            <span class="rus">Я нахожусь в комнате.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="2">The<b> book is</b> on the table.</span><br />
            <span class="rus">Книга лежит на столе.</span></div></div>
    <p>
        В данных примерах <strong>глагол to be</strong> является самостоятельным глаголом. Так же, как и в русском языке, глагол <strong>to be</strong> может быть глаголом-связкой в именном сказуемом (в значении &quot;есть&quot;).</p>
    <p>
        В отличие от русского языка, в английском языке глагол-связка никогда не опускается, поскольку английское предложение имеет строго фиксированный <a href="/grammar/word-order">порядок слов</a>: <b>подлежащее</b> (subject) + <b>сказуемое</b> (verb) + <b>дополнение</b> (object)</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="3">I <b>am</b> a doctor.</span><br />
            <span class="rus">Я врач. (Я есть врач.)</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="4">The weather <b>is </b>bad.</span><br />
            <span class="rus">Погода плохая.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="5">They <b>are </b>from Paris.</span><br />
            <span class="rus">Они из Парижа.</span></div></div>
    <p>
        Итак, <strong>глагол-связка to be</strong> в английском предложении никогда не опускается, т.к. он входит в именное сказуемое, и его место после подлежащего. На русский же язык глагол <strong>to be</strong> в данных случаях не переводится:</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="6"><b>I am</b> happy.</span><br />
            <span class="rus">Я счастлив.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="7">The <b>book is</b> interesting.</span><br />
            <span class="rus">Книга интересная.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="8"><b>He is</b> our teacher.</span><br />
            <span class="rus">Он наш учитель.</span></div></div>
    <div id="spot-adcom"></div><p>
        <strong>Глагол to be</strong> не требует вспомогательного глагола для образования вопросительной или отрицательной формы.</p>
    <p>
        Чтобы задать вопрос нужно поставить глагол <strong>to be</strong> перед подлежащим:</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="9"><b>Am</b> I happy?</span><br />
            <span class="rus">Я счастлив?</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="10"><b>Is</b> the book interesting?</span><br />
            <span class="rus">Книга интересная?</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="11"><b>Is</b> he our teacher?</span><br />
            <span class="rus">Он наш учитель?</span></div></div>
    <p>
        Для образования отрицательной формы достаточно поставить отрицательную частицу <b>not</b> после глагола <strong>to be</strong>:</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="12">I <b>am not</b> happy.</span><br />
            <span class="rus">Я не счастлив.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="13">The book <b>is not</b> interesting.</span><br />
            <span class="rus">Книга не интересная.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="14">He <b>is not</b> our teacher.</span><br />
            <span class="rus">Он не наш учитель.</span></div></div>
    <p>
        В разговорной речи отрицательная частица <b>not</b> часто сливается с глаголом <strong>to be</strong>, образуя сокращения:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="15">is not</span> = <span class="eng sound-word" data-word="16">isn&#39;t</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="17">are not</span> = <span class="eng sound-word" data-word="18">aren&#39;t</span>
        </div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="19">They aren&#39;t stupid.</span><br />
            <span class="rus">Они не глупые.</span></div>
    </div>
    <p>
        Также глагол <strong>to be</strong> может сокращаться, сливаясь с <a href="/grammar/personal-pronouns">личным местоимением</a>:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="20">I am</span> = <span class="eng sound-word" data-word="21">I&#39;m</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="22">We are</span> = <span class="eng sound-word" data-word="23">we&#39;re</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="24">He is</span> = <span class="eng sound-word" data-word="25">he&#39;s</span>
        </div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="26">He&#39;s at home.</span><br />
            <span class="rus">Он дома.</span></div>
    </div>

