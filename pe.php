<h1 class="title">Переходные и непереходные английские глаголы</h1>

<div id="partnerPnt"></div>

<div class="article" data-sound-type="grammar" data-sound-file="verbs-transition">


    <p>
        Глаголы в английском языке, также, как и в русском, делятся на переходные и непереходные.</p>
    <p>
        <strong>Переходные глаголы</strong> обозначают действие, которое по своему смыслу переходит на некий предмет, который может быть выражен прямым дополнением (переводится на русский как дополнение в винительном падеже без предлога).</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="1">The girl is <b>writing</b> a letter.</span><br />
            <span class="rus">Девочка пишет (что?) письмо.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="2">He <b>invited</b> me to the concert.</span><br />
            <span class="rus">Он пригласил (кого?) меня на концерт.</span></div></div>
    <p>
        <strong>Непереходные глаголы</strong> выражают действие, которое не переходит непосредственно на некий предмет, они не могут иметь при себе прямого дополнения.</p>
    <div id="spot-adcom"></div><p>
        Например, &laquo;собирать&raquo; можно только что-то: марки, грибы или автомобиль, само значение глагола подразумевает наличие дополнения. Такие глаголы, как &laquo;путешествовать&raquo; и &laquo;отдыхать&raquo;, такого дополнения не требуют.</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="3">My friend <b>lives</b> in Kiev.</span><br />
            <span class="rus">Мой друг живет в Киеве.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="4">My father <b>arrived</b> yesterday.</span><br />
            <span class="rus">Мой отец приехал вчера.</span></div></div>
    <p>
        Некоторые <a href="/grammar/english-verbs">английские глаголы</a> могут использоваться как в переходном, так и в непереходном значении (<span class="eng">to begin, to open, to roll, to move, to stop, to change</span>):</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="5">Our mood started to <b>change</b> for the better.</span> <span class="rus">(непереходный)</span><br />
            <span class="rus">Наше настроение начало меняться к лучшему.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="6">The bank has <b>changed</b> its working hours.</span> <span class="rus">(переходный)</span><br />
            <span class="rus">Банк изменил (что?) свои часы работы.</span></div></div>
