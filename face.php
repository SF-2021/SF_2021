        </tr>
        <tr>
            <td>
                He / She / It has</td>
            <td>
                They have</td>
        </tr>
        </tbody>
    </table>
    <div id="spot-adcom"></div><ul class="list">
        <li class="list__item">
            <a href="/grammar/verb-to-be">Глагол <b>to be</b></a>, который имеет отдельные формы для 1 и 3 лица ед. ч., отличающиеся от форм множественного числа:</li>
    </ul>
    <table class="table table_bordered table_centered">
        <tbody>
        <tr>
            <td colspan="2">
                <b>Формы глагола to be</b></td>
        </tr>
        <tr>
            <td>
                I am</td>
            <td>
                We are</td>
        </tr>
        <tr>
            <td>
                You are</td>
            <td>
                You are</td>
        </tr>
        <tr>
            <td>
                He / She / It is</td>
            <td>
                They are</td>
        </tr>
        </tbody>
    </table>
    <p>
        Кроме этого, глагол <b>to be</b> в прошедшем времени имеет форму <b>was </b>для единственного числа и <b>were </b>для множественного числа.</p>
