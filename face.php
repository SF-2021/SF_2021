<h1 class="title">Лицо и число английского глагола</h1>

<div id="partnerPnt"></div>

<div class="article" data-sound-type="grammar" data-sound-file="verbs-person-number">


    <p>
        <a href="/grammar/english-verbs">Английский глагол</a> имеет два числа (единственное и множественное) и три лица &ndash; первое <span class="rus">(я/мы)</span>, второе <span class="rus">(ты/вы) </span>и третье <span class="rus">(он/она/оно/они)</span>.</p>
    <p>
        Тем не менее, глагол может их выражать только в определенных случаях.</p>
    <p>
        Например, 3-е лицо единственного числа настоящего времени можно определить по окончанию <b>-s </b>(<b>-es</b>):</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="1"><b>I play</b> golf. <b>He plays</b> basketball.</span><br />
            <span class="rus">Я играю в гольф. Он играет в баскетбол.</span></div></div>
    <p>
        Исключениями из этого правила являются:</p>
    <ul class="list">
        <li class="list__item">
            <a href="/grammar/modal-verbs">Модальные глаголы</a>, к которым нельзя прибавлять окончание <b>-s</b>/<b>-es</b>:</li>
    </ul>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="2">He <b>can</b> swim faster.</span><br />
            <span class="rus">Он может плавать быстрее.</span></div></div>
    <ul class="list">
        <li class="list__item">
            <a href="/grammar/verb-to-have">Глагол <b>to have</b></a>, который в 3 лице ед. ч. имеет форму <b>has</b>:</li>
    </ul>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="3"><b>He has</b> many medals.</span><br />
            <span class="rus">У него много медалей.</span></div></div>
    <table class="table table_bordered table_centered">
        <tbody>
        <tr>
            <td colspan="2">
                <b>Формы глагола to have</b></td>
        </tr>
