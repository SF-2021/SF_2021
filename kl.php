<h1 class="title">Английские глаголы. Классификация глаголов английского языка.</h1>

<div id="partnerPnt"></div>

<div class="article" data-sound-type="grammar" data-sound-file="english-verbs">


    <p>
        <strong>Глагол</strong> &ndash; это самостоятельная часть речи, обозначающая действие или состояние предмета или лица.<br />
        В английском языке признаком глагола в неопределенной форме является частица <b>to</b>.</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="1">to sing</span> &ndash; <span class="rus">петь</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="2">to draw</span> &ndash; <span class="rus">рисовать</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="3">to exist</span> &ndash; <span class="rus">существовать</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="4">to stand</span> &ndash; <span class="rus">стоять</span>
        </div>
    </div>
    <p>
        Чаще всего именно глагол является ключевой основой, вокруг которой строится предложение, поэтому глаголы почти во всех языках следуют сложным правилам грамматики и имеют большое число форм.</p>
    <div id="spot-adcom"></div><h2 class="mtitle">
        Структура английских глаголов</h2>
    <p>
        По структуре английские глаголы делятся на простые, производные, сложные и составные.</p>
    <p>
        <b>Простые</b> глаголы состоят только из одного корня:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="5">walk</span> &ndash; <span class="rus">идти</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="6">play</span> &ndash; <span class="rus">играть</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="7">eat</span> &ndash; <span class="rus">есть</span>
        </div>
    </div>
    <p>
        В <b>производных</b> глаголах, кроме корня, есть приставка и/или суффикс:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="8"><b>un</b>load</span> &ndash; <span class="rus">разгружать</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="9"><b>re</b>read</span> &ndash; <span class="rus">перечитать</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="10"><b>re</b>play</span> &ndash; <span class="rus">переиграть</span>
        </div>
    </div>
    <p>
        <b>Сложные</b> глаголы состоят из двух основ:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="11">underline</span> &ndash; <span class="rus">подчёркивать</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="12">whitewash</span> &ndash; <span class="rus">белить</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="13">overtake</span> &ndash; <span class="rus">догонять</span>
        </div>
    </div>
    <p>
        <b>Составные</b> (фразовые) глаголы состоят из глагола и наречия или предлога:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="14">look out</span> &ndash; <span class="rus">высматривать</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="15">stand up</span> &ndash; <span class="rus">выдерживать</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="16">pull off</span> &ndash; <span class="rus">снимать</span>
        </div>
    </div>
    <p>
        По значению английские глаголы делятся на смысловые и служебные.</p>
    <p>
        <b>Смысловые</b> (самостоятельные) глаголы обладают собственным лексическим значением, они обозначают определенное действие или состояние:</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="17">I <b>work</b> at the zoo.</span><br />
            <span class="rus">Я работаю в зоопарке.</span></div></div>
    <p>
        <b>Служебные</b> глаголы не имеют самостоятельного значения. Они используются только для построения сложных форм глагола или составных сказуемых. В составных сказуемых спрягаются именно служебные глаголы, то есть при их помощи можно выразить лицо, число и время:</p>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="18">I <b>am</b> going</span> &ndash; <span class="rus">Я иду</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="19">Jim <b>is</b> going</span> &ndash; <span class="rus">Джим идет</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="20">people <b>were</b> going</span> &ndash; <span class="rus">люди шли</span>
        </div>
    </div>
    <h2 class="mtitle">
        Служебные английские глаголы:</h2>
    <ul class="list">
        <li class="list__item">
            <b>глаголы-связки</b>, соединяющие в предложении подлежащее с определенным состоянием (обычно выражаемым дополнением):</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="21">to be</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="22">to become</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="23">to seem</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="24">to look</span>
        </div>
        <div class="example__line">и др.</div>
    </div>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="25">She <b>got</b> hungry in the evening.</span><br />
            <span class="rus">Она проголодалась вечером.</span></div></div>
    <ul class="list">
        <li class="list__item">
            <b>вспомогательные глаголы</b>, которые служат для образования сложных глагольных форм:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="21">to be</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="27">to do</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="28">to have</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="29">will</span>
        </div>
        <div class="example__line">и др.</div>
    </div>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="30">We <b>have seen</b> it before.</span><br />
            <span class="rus">Мы видели это раньше.</span></div></div>
    <ul class="list">
        <li class="list__item">
            <b>модальные глаголы</b>, которые отражают отношение говорящего к данному действию:</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="31">can</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="32">may</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="33">must</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="34">need</span>
        </div>
        <div class="example__line">и др.</div>
    </div>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="35">He <b>must</b> sing loudly.</span><br />
            <span class="rus">Он должен петь громко.</span></div></div>
    <h2 class="mtitle">
        Основные формы английского глагола:</h2>
    <ul class="list">
        <li class="list__item">
            <b>инфинитив</b></li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="1">to sing</span> &ndash; <span class="rus">петь</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="37">to travel</span> &ndash; <span class="rus">путешествовать</span>
        </div>
    </div>
    <ul class="list">
        <li class="list__item">
            <b>прошедшее неопределенное время</b></li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="38">sang</span> &ndash; <span class="rus">пел</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="39">travelled</span> &ndash; <span class="rus">путешествовал</span>
        </div>
    </div>
    <ul class="list">
        <li class="list__item">
            <b>причастие прошедшего времени</b> (причастие второе)</li>
    </ul>
    <div class="example">
        <div class="example__line">
            <span class="eng sound-word" data-word="40">sung</span> &ndash; <span class="rus">спевший</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="39">travelled</span> &ndash; <span class="rus">путешествовавший</span>
        </div>
    </div>
    <p>
        Иногда их называют соответственно первой, второй и третьей формой английского глагола. Эти основные формы служат для построения всех остальных глагольных форм, простых и сложных.</p>
