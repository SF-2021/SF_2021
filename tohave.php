                <td>
                    had</td>
                <td>
                    shall have<br />
                    will have</td>
            </tr>
            </tbody>
        </table>
    </div>
    <p>
        Значение этого глагола - &quot;иметь, владеть, обладать&quot;. Часто в разговорной речи вместо <b>have</b>, <b>has </b>употребляется словосочетание <b>have got</b>, <b>has got</b> (краткие формы<b> &#39;ve got</b> и <b>&#39;s got</b>) с тем же значением, особенно когда речь идёт о временном владении или только что приобретённом предмете или предметах:</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="1">We<b>&#39;ve got</b> a nice flat.</span><br />
            <span class="rus">У нас хорошая квартира.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="2"><b>Have </b>you <b>got </b>any pets?</span><br />
            <span class="rus">У вас есть домашние животные?</span></div></div>
    <div id="spot-adcom"></div><p>
        В вопросительной форме в британском варианте языка <strong>глагол to have</strong> часто стоит перед подлежащим, в американском варианте вопросительная и отрицательная формы всегда образуются с помощью вспомогательного глагола <b>do</b>:</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="3"><b>Have </b>you two sons? </span><span class="rus">(брит.)</span><br />
            <span class="rus">У вас есть два сына?</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="4"><b>Do </b>you <b>have </b>a lot of free time?</span> <span class="rus">(амер.)</span><br />
            <span class="rus">У вас много свободного времени?</span></div></div>
    <p>
        Отрицательные предложения строятся с помощью формы глагола <strong>to have</strong> с отрицанием <b>not</b> или с отрицательным местоимением <b>no</b>:</p>
    <div class="example">
        <div class="example__line example__line_sound">
            <span class="eng sound-word" data-word="5">I <b>haven&#39;t got</b> a pen.</span><br />
            <span class="rus">У меня нет ручки.</span></div>
        <div class="example__line example__line_sound example__line_break">
            <span class="eng sound-word" data-word="6">I <b>have got no</b> problem with that.</span><br />
            <span class="rus">У меня нет никаких проблем с этим.</span></div>
        <div class="example__line example__line_break">
            <span class="eng sound-word" data-word="7">have not</span> = <span class="eng sound-word" data-word="8">haven&#39;t</span>
        </div>
        <div class="example__line">
            <span class="eng sound-word" data-word="9">has not</span> = <span class="eng sound-word" data-word="10">hasn&#39;t</span>
        </div>
    </div>
