<h1 class="title">Глагол to have в английском языке</h1>

<div id="partnerPnt"></div>

<div class="article" data-sound-type="grammar" data-sound-file="verb-to-have">


    <p>
        Как самостоятельный глагол <strong>to have</strong> в настоящем времени имеет 2 формы:</p>
    <ul class="list">
        <li class="list__item">
            <strong>have</strong> для всех лиц, кроме 3-го лица единственного числа</li>
        <li class="list__item">
            <strong>has</strong> для 3-го лица единственного числа</li>
    </ul>
    <p>
        В прошедшем времени <strong>глагол to have</strong> имеет форму <b>had</b>, в будущем - <b>shall have</b>, <b>will have</b>.</p>
    <h2 class="mtitle">
        Формы глагола to have</h2>
    <div class="scroll-table">
        <table class="table table_bordered table_centered">
            <tbody>
            <tr>
                <td colspan="2">
                    <span class="rus">настоящее время</span></td>
